void ssd1306_draw_char(uint8_t x, uint8_t page, char c) {
    if (page > 7 || c < 32 || c > 127) return;
    const uint8_t *glyph = g_font_5x8[(uint8_t)c - 32];
    for (uint8_t col = 0; col < 5; col++) {
        if (x + col >= SSD1306_WIDTH) break;
        g_fb[x + col + (uint16_t)page * SSD1306_WIDTH] = glyph[col];
    }
    /* 1-pixel inter-char spacing */
    if (x + 5 < SSD1306_WIDTH) {
        g_fb[x + 5 + (uint16_t)page * SSD1306_WIDTH] = 0;
    }
}

/* 2x scaled 5x8 char → 10x16 pixels. y = pixel row (0-63). */
void ssd1306_draw_big_char(uint8_t x, uint8_t y, char c) {
    if (c < 32 || c > 127) return;
    const uint8_t *g = g_font_5x8[(uint8_t)c - 32];
    for (uint8_t col = 0; col < 5; col++) {
        for (uint8_t row = 0; row < 8; row++) {
            if (g[col] & (1 << row)) {
                uint8_t bx = (uint8_t)(x + col * 2);
                uint8_t by = (uint8_t)(y + row * 2);
                ssd1306_draw_pixel(bx,     by,     1);
                ssd1306_draw_pixel(bx + 1, by,     1);
                ssd1306_draw_pixel(bx,     by + 1, 1);
                ssd1306_draw_pixel(bx + 1, by + 1, 1);
            }
        }
    }
}

/* Draw time-colon (two 3x3 dots) at position (x,y), 16 pixels tall */
void ssd1306_draw_big_colon(uint8_t x, uint8_t y) {
    for (uint8_t dx = 0; dx < 3; dx++) {
        for (uint8_t dy = 0; dy < 3; dy++) {
            ssd1306_draw_pixel((uint8_t)(x + dx), (uint8_t)(y + 3 + dy), 1);
            ssd1306_draw_pixel((uint8_t)(x + dx), (uint8_t)(y + 10 + dy), 1);
        }
    }
}

void ssd1306_draw_string(uint8_t x, uint8_t page, const char *s) {
    while (*s && x < SSD1306_WIDTH) {
        ssd1306_draw_char(x, page, *s);
        x += 6;
        s++;
    }
}

/* Bresenham line */
void ssd1306_draw_line(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1) {
    int16_t dx = (int16_t)x1 - x0; if (dx < 0) dx = -dx;
    int16_t dy = (int16_t)y1 - y0; if (dy < 0) dy = -dy;
    int16_t sx = (x0 < x1) ? 1 : -1;
    int16_t sy = (y0 < y1) ? 1 : -1;
    int16_t err = dx - dy;
    int16_t x = x0, y = y0;

    while (1) {
        ssd1306_draw_pixel((uint8_t)x, (uint8_t)y, 1);
        if (x == x1 && y == y1) break;
        int16_t e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x += sx; }
        if (e2 <  dx) { err += dx; y += sy; }
    }
}
