/* ── I2C write primitive ────────────────────────────────────── */
static void i2c_write_cmd(uint8_t cmd) {
    while (I2C1->SR2 & I2C_SR2_BUSY);

    I2C1->CR1 |= I2C_CR1_START;
    while (!(I2C1->SR1 & I2C_SR1_SB));

    I2C1->DR = 0x78;  /* addr 0x3C << 1, write */
    while (!(I2C1->SR1 & I2C_SR1_ADDR));
    (void)I2C1->SR2;

    I2C1->DR = 0x00;  /* control byte: command */
    while (!(I2C1->SR1 & I2C_SR1_TXE));

    I2C1->DR = cmd;
    while (!(I2C1->SR1 & I2C_SR1_BTF));

    I2C1->CR1 |= I2C_CR1_STOP;
}

static void i2c_write_data(const uint8_t *data, uint16_t len) {
    while (I2C1->SR2 & I2C_SR2_BUSY);

    I2C1->CR1 |= I2C_CR1_START;
    while (!(I2C1->SR1 & I2C_SR1_SB));

    I2C1->DR = 0x78;
    while (!(I2C1->SR1 & I2C_SR1_ADDR));
    (void)I2C1->SR2;

    I2C1->DR = 0x40;  /* control byte: data */
    while (!(I2C1->SR1 & I2C_SR1_TXE));

    for (uint16_t i = 0; i < len; i++) {
        I2C1->DR = data[i];
        while (!(I2C1->SR1 & I2C_SR1_TXE));
    }

    while (!(I2C1->SR1 & I2C_SR1_BTF));
    I2C1->CR1 |= I2C_CR1_STOP;
}

/* ── Framebuffer ─────────────────────────────────────────────── */
static uint8_t g_fb[SSD1306_WIDTH * SSD1306_PAGES];  /* 128 × 8 = 1024 bytes */

/* ── Initialization sequence ─────────────────────────────────── */
void ssd1306_init(void) {
    static const uint8_t init_cmds[] = {
        0xAE,           /* display off */
        0xD5, 0x80,     /* clock div */
        0xA8, 0x3F,     /* multiplex = 64 */
        0xD3, 0x00,     /* display offset = 0 */
        0x40,           /* start line = 0 */
        0x8D, 0x14,     /* charge pump on */
        0x20, 0x00,     /* horizontal addressing */
        0xA1,           /* segment remap (flip X) */
        0xC8,           /* COM scan direction (flip Y) */
        0xDA, 0x12,     /* COM pins alt */
        0x81, 0xCF,     /* contrast */
        0xD9, 0xF1,     /* pre-charge */
        0xDB, 0x40,     /* VCOMH deselect */
        0xA4,           /* output follows RAM */
        0xA6,           /* normal (non-inverted) */
        0xAF,           /* display on */
    };

    for (uint32_t i = 0; i < sizeof(init_cmds); i++) {
        i2c_write_cmd(init_cmds[i]);
    }

    ssd1306_clear();
    ssd1306_refresh();
}

void ssd1306_clear(void) {
    for (uint16_t i = 0; i < sizeof(g_fb); i++) {
        g_fb[i] = 0x00;
    }
}

/* Flush entire framebuffer to display using horizontal addressing */
void ssd1306_refresh(void) {
    i2c_write_cmd(0x21);  /* column start/end */
    i2c_write_cmd(0x00);
    i2c_write_cmd(127);
    i2c_write_cmd(0x22);  /* page start/end */
    i2c_write_cmd(0x00);
    i2c_write_cmd(7);
    i2c_write_data(g_fb, sizeof(g_fb));
}

void ssd1306_draw_pixel(uint8_t x, uint8_t y, uint8_t color) {
    if (x >= SSD1306_WIDTH || y >= SSD1306_HEIGHT) return;
    uint16_t idx = x + ((uint16_t)(y >> 3) * SSD1306_WIDTH);
    if (color) g_fb[idx] |=  (1U << (y & 7));
    else       g_fb[idx] &= ~(1U << (y & 7));
}

